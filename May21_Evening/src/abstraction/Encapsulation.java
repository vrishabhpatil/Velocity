package abstraction;

public class Encapsulation {
     String name;
     String id;
     int age;
     public String getname() {
    	 return name;
     }
     public String getid() {
    	 return id;
     }
     public int getage() {
    	 return age;
     }
     public void setname(String name) {
    	 this.name=name;
     }
     public void setid(String id) {
    	 this.id=id;
     }
     public void setage(int age) {
    	 this.age=age;
     }
}
